"""
Ejercicio 019
Escribir un programa que permita ingresar dos números enteros e indicar si son iguales o distintos."""

numero1 = int(input("Ingrese primer número: "))
numero2 = int(input("Ingrese segundo número: "))

if numero1 == numero2:
    print("Son iguales!")
else:
    print("Son distintos!")

