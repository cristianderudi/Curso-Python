"""Ejercicio 002
Escribir un programa que solicite al usuario su nombre y su edad, y 
luego muestre por pantalla un mensaje que diga "Hola, [nombre]. Tu edad es [edad] años."""


nombre = input("Ingrese su nombre: ")
edad = int(input("Ingrese su edad: "))

print(f"Hola, {nombre}.")
print(f"Tu edad es de : {edad} años.")