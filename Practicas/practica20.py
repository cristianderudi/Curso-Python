""" Ejercicio 020
Escribir un programa que permita ingresar dos cadenas de caracteres e indicar si son iguales o distintas. """


cadena1 = input("Ingrese primera cadena de texto: ")
cadena2 = input("Ingrese segunda cadena de texto: ")

if cadena1 == cadena2:
    print("Son iguales!")
else:
    print("Son distintas!")
    